/*-----------------------------------------------------------------------------------*
* FUNCTION : Hash_Elf( const char * apString )
* ACTION   : Hash_Elf
* CREATION : 21.01.2003 PNK
*-----------------------------------------------------------------------------------*/

U32	Hash_Elf( const char * apString )
{
	U32	lHash;
	U32	lTemp;

	lHash = 0;
	while ( *apString )
	{
		lHash = ( lHash << 4L ) + *apString++;
		lTemp = lHash & 0xF0000000L;
		if( lTemp )
		{
			lHash ^= lTemp >> 24L;
		}
		lHash &= ~lTemp;
	}

	return( lHash );
}


